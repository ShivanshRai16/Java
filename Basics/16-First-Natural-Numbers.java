public class Practice2 {
    public static void main(String[] args) {
        int i = 1;
        while (i<=100){
            System.out.println(i);
            i++;
        }
        
        // Using do while loop
        int a = 0;
        do {
            System.out.println(a);
            a++;
        } while (a<5);
    }
}
