public class Practice2 {
    public static void main(String[] args) {
        //Using while Loop
        int i = 1;
        while (i<=100){
            System.out.println(i);
            i++;
        }
        
        // Using do while loop
        int a = 0;
        do {
            System.out.println(a);
            a++;
        } while (a<5);
        
        // Using for loop
        for (int i=0; i<=10; i++){
            System.out.println(i);
        }
    }
}
